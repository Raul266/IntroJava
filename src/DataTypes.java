public class DataTypes {
    public static void main(String[] args) {
        // cele mai folosite 5 tipuri de data

        // String = sir de caractere delimitate de " "
        String marca = "Dacia";
        String model = "1310";
        String model_nou = "Duster";

        // aflam dimensiunea stringului
        System.out.println(marca.length());

        // scriem tot stringul cu litere mari
        System.out.println(marca.toUpperCase());

        // integer = numar intreg (int)
        int an_fabricatie = 1985;

        // double  =  numar zecimal
        double pret = 2300.50;

        // boolean = adevarat sau fals
        boolean inmatriculat = true;

        // char =  un singur caracter
        char moneda = 'E';
    }
}
